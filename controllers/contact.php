class Contact
{
    /**
    * Contact Page
    *
    * @return void
    **/
    public static function index()
    {
        $app = Slim::getInstance();
        return $app->render('contact.html');
    }
    
    /**
    * About
    *
    * @return void
    **/
    public static function about()
    {
        $app = Slim::getInstance();
        $app->view()->appendData(
            array(
            'active' => 'about',
            )
        );
        return $app->render('about.html');
    }
    

}




